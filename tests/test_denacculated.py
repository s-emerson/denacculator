import unittest

from denacc import nacc2redcap
from denacc import convert_field_list
from denacc import add_fields
from denacc import add_field_values


class TestDenacculated(unittest.TestCase):
    """
    Tests that each leg of the denacculator process is working properly
    """

    def setUp(self):
        self.expected_fields = set_dict()
        self.expected_field_list = list(self.expected_fields.keys())

    def test_for_current_field_list(self):
        """ 
        Tests that field names are being converted properly using
        the dictionary below
        """
        result = convert_field_list.convert_fields()
        self.assertEqual(self.expected_field_list, result)

    def test_for_fields_deleted(self):
        """ Tests that the non-REDCap fields are deleted """
        expected_list = add_fields.list_fields()
        expected_list.remove('ptid')
        expected_list.remove('redcap_event_name')
        expected_list.remove('nacc_derived_values_complete')
        result = nacc2redcap.delete_values(self.expected_fields)
        result_list = list(result.keys())
        self.assertEqual(expected_list, result_list)

    def test_for_fields_added(self):
        """ Tests that the REDCap-required fields are added """
        added_fields = add_field_values.add_fields(self.expected_fields)

        e = self.expected_fields
        e['ptid'] = e['ndv_ptid']
        e['redcap_event_name'] = ''
        e['nacc_derived_values_complete'] = '2'
        del e['ndv_visitnum']
        del e['ndv_naccmoca']
        del e['ndv_adgcexom']
        del e['ndv_ngdsgwas']
        del e['ndv_ngdsexom']
        del e['ndv_ngdswes']
        del e['ndv_ngdswgs']
        del e['ndv_ngdsgwac']
        del e['ndv_ngdsexac']
        del e['ndv_ngdsweac']
        del e['ndv_ngdswgac']
        del e['ndv_adgcexr']
        del e['ndv_naccspnl']
        del e['ndv_naccengl']
        del e['ndv_apreflan']
        del e['ndv_ayrspan']
        del e['ndv_ayrengl']
        del e['ndv_apcspan']
        del e['ndv_apcengl']
        del e['ndv_aspkspan']
        del e['ndv_areaspan']
        del e['ndv_awrispan']
        del e['ndv_aundspan']
        del e['ndv_aspkengl']
        del e['ndv_areaengl']
        del e['ndv_awriengl']
        del e['ndv_aundengl']

        self.assertEqual(added_fields, e)

    def test_fill_event_name(self):
        """ Tests that the program is able to properly identify redcap_event_name based on ndv_naccvnum (value '3' given in dict) """
        expected = 'followup_visit_yea_arm_1b'
        added_fields = self.expected_fields
        added_fields['ptid'] = added_fields['ndv_ptid']
        added_fields['redcap_event_name'] = ''
        added_fields['nacc_derived_values_complete'] = '2'

        result_row = nacc2redcap.fill_extra_columns(added_fields)
        result = result_row['redcap_event_name']

        self.assertEqual(expected, result)


